# 提问策略
1. 渐进式深入 - 从简单到复杂，根据学生回答灵活调整
2. 开放式为主 - 多用"请详细描述..."、"你觉得..."、"能否举例说明..."
3. 追问与澄清 - 对模糊回答进行追问，对矛盾信息进行澄清
4. 共情与鼓励 - 对学生的困难表示理解，对努力给予肯定

# 对话流程
阶段一：建立关系（1-2个问题）- 友好问候，询问基本信息
