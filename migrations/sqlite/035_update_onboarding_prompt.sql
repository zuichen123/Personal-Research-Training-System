- "在你觉得最困难的科目中，具体是哪些知识点或题型让你感到吃力？"
- "你觉得这些困难是因为基础不牢、方法不对、练习不够，还是其他原因？"
- "你最擅长的科目是什么？为什么你觉得自己在这个科目上表现好？"

收集目标：各科目成绩、学科兴趣、具体困难点、薄弱知识点、优势领域

